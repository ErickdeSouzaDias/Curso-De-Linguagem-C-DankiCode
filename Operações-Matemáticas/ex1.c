// Faça um programa que some três valores e mostre o resultado na tela.
#include <stdio.h>
#include <stdlib.h>

int main(){

    // Armazenando os valores nas variáveis.
    int num1 = 2;
    int num2 = 4;
    int num3 = 6;

    // Somando os valores das variáveis
    int soma = num1 + num2 + num3;

    // Exibindo a soma dos valores das variáveis.
    printf("A soma %d + %d + %d é igual a %d.", num1, num2, num3, soma);

    return 0;
}
